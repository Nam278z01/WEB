$("#loginForm .btn--primary").click(function () {
    let email = $("#loginForm input[type=text]").val()
    let password = $("#loginForm input[type=password]").val()
    if (signIn(email, password)) {
        $('body, html').scrollTop(0)
        sessionStorage.setItem("login", "true")
        location.reload()
    }
    else {
        $("#loginForm .auth-form__form p").css("display", "block")
        $("#loginForm .auth-form__form input").css("border-bottom", "1px solid red")
    }
})

